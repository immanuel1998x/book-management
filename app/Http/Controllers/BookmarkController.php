     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $bookmarks = Book::where('user_id', Auth::id())->where('owned', false)->orderBy('updated_at', 'DESC')->paginate(8);
        
        if (count($bookmarks) > 0) {
            return view('bookmark.index', compact('bookmarks'));
        } else {
            return redirect('/bookmark/create')->with('error', 'Please add some bookmark.');
        }
    }

